package Lesson_5;

public class staff {
    String firstLastName;
    String position;
    String email;
    int phone;
    int salary;
    int age;

    public staff (String firstLastName, String position, String email, int phone, int salary, int age) {
        this.firstLastName = firstLastName;
        this.position = position;
        this.email = email;
        this.phone = phone;
        this.salary = salary;
        this.age = age;
    }
}
